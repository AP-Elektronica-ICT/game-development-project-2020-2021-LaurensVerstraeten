        private int coinsCollected = 0;
        
        Shadow shadow;
        Level levelOne;
        Level levelTwo;
        Level currentLevel;

        public GamePlay(Game1 game, GraphicsDevice graphicsDevice, ContentManager content) : base(game, graphicsDevice, content)
        {
            levelOne = new Level(mapLevelOne, _content);
            currentLevel = levelOne;
            levelTwo = new Level(mapLevelTwo, _content);
            currentLevel.CreateWorld(25);

            Global.spriteBatch = new SpriteBatch(graphicsDevice);
            textureBackground = _content.Load<Texture2D>("greenhill");
            texture = _content.Load<Texture2D>("ShadowSprite");

            InitializeGameObject();                       
        }

        private void InitializeGameObject()
        {
            shadow = new Shadow(texture, new KeyBoardReader());
        }

        public override void Draw(GameTime gameTime, SpriteBatch spriteBatch)
        {
            Global.spriteBatch.Begin();

            // TODO: Add sprites

            Global.spriteBatch.Draw(textureBackground, new Rectangle(0, 0, Global.screenWidth, Global.screenHeight), Color.White);
            currentLevel.DrawWorld(Global.spriteBatch);
            shadow.Draw(Global.spriteBatch);

            Global.spriteBatch.End();
        }               

        public override void Update(GameTime gameTime)
        {
            shadow.Update(gameTime);

            //world classe maken
            foreach (Block tile in currentLevel.CollisionTiles)
            {
                shadow.Collision(tile.Rectangle, currentLevel.Width, currentLevel.Height);
            }

            foreach (Coin coin in currentLevel.CollisionCoins)
            {
                if (CollisionManager.TouchCoin(shadow.hitBox, coin.Rectangle))
                {

                    coin.isCollected = true;
                    coin.Update();
                    coinsCollected++;

                    if (coinsCollected == currentLevel.CollisionCoins.Count)
                    {
                        if (currentLevel == levelTwo)
                        {
                            //victory
                        }
                        currentLevel = levelTwo;
                        Global.reset = true;
                        currentLevel.CreateWorld(25);
                    }
                }
            }

            if (shadow.Position.Y > Global.screenHeight)
            {
                _game.ChangeState(new GameOver(_game, _graphicsDevice, _content));
            }           
            
        }

